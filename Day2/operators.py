
x = 6
y = 3
z = 7
n = 9


print(f"{x} divide  {y} = {x / y}")  # 2.0 float
print(f"{z} divided to the floor  {y} equals {z // y}")  # 7 // 3 = 2
print(f"{z} modulo {y} equals {z % y}")  # 7 % 3 = 1
print(f"{x} to the power of {y} equals {x ** y}")  # 6 ** 3 = 216
print(f"Square root of {n} is {n ** 0.5}")  # 9 ** 0.5 = 3.0

